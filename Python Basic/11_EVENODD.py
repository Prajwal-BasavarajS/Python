# Given a positive integer x. Your task is to check, if it is even or odd (Any number that gives zero as remainder when divided by 2 is an even number)

# Examples

# Input: x = 4
# Output: Even
# Input: x = 5
# Output: Odd

n = int(input())
if n % 2 == 0:
    print("Even")
else : 
    print("odd")
